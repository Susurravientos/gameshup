package matchpubg;

public class RosterAttributes {
	private String shardId;
	private RosterAttributesStats stats;
	private String won;
	public String getShardId() {
		return shardId;
	}
	public void setShardId(String shardId) {
		this.shardId = shardId;
	}
	public RosterAttributesStats getStats() {
		return stats;
	}
	public void setStats(RosterAttributesStats stats) {
		this.stats = stats;
	}
	public String getWon() {
		return won;
	}
	public void setWon(String won) {
		this.won = won;
	}
	
	
}

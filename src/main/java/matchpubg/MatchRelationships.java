package matchpubg;

public class MatchRelationships {
	private MatchRelationshipsAssets assets;
	private MatchRelationshipsRosters rosters;
	private MatchRelationshipsRounds rounds;
	private MatchRelationshipsSpectators spectators;
	
	
	public MatchRelationshipsAssets getAssets() {
		return assets;
	}
	public void setAssets(MatchRelationshipsAssets assets) {
		this.assets = assets;
	}
	public MatchRelationshipsRosters getRosters() {
		return rosters;
	}
	public void setRosters(MatchRelationshipsRosters rosters) {
		this.rosters = rosters;
	}
	public MatchRelationshipsRounds getRounds() {
		return rounds;
	}
	public void setRounds(MatchRelationshipsRounds rounds) {
		this.rounds = rounds;
	}
	public MatchRelationshipsSpectators getSpectators() {
		return spectators;
	}
	public void setSpectators(MatchRelationshipsSpectators spectators) {
		this.spectators = spectators;
	}

	
}
